# Требуется высадить аллею из n деревьев. Лунки под деревья пронумерованы последовательно. Расстояние между деревьями определяется как разность номеров лунок, где они растут.
# На Марсе могут выжить только k сортов деревьев. Красота одного дерева i-го сорта — c. Требуется озеленить Марс с максимальной суммарной красотой. Однако, деревья одного сорта конфликтуют, поэтому их следует садить как минимум на расстоянии k.
# Формат ввода
# В первой строке находятся два целых числа 
# k(1≤k≤5) и n(1≤n≤105). В следующей строке следуют k целых чисел c(1≤c≤105).
# Формат вывода
# В единственной строке выведите ответ на задачу.

