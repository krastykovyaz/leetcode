# print(beauty)
def main(k, n, c):
    k, n = map(int, input().split())
    c = list(map(int, input().split()))
    dp = [0] * (n+1)
    dp[0] = 0

    for i in range(1, n+1):
        if k >= i:
            dp[i] = c[i-1]
        else:
            for j in range(1, k+1):
                dp[i] = max(dp[i], dp[i-k+j-1] + c[j-1])

    print(dp[n])




